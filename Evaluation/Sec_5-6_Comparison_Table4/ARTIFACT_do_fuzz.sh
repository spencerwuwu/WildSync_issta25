#!/bin/bash -e

if [ -z $1 ]; then
    echo "Usage: $0 <experiment_name>"
    exit 1
fi

# load the configuration file (captainrc)
rc=$1

set -a
source "$rc"
set +a
mkdir -p ${WORKDIR}

# Run the fuzzing trials
echo "==================================="
echo "==  Starting fuzzing trials"
echo "==================================="
./run.sh $rc | tee ${WORKDIR}/run.log

# Translate the crash files
echo "==================================="
echo "==  Translating fuzzing results"
echo "==================================="
./do_translate.sh $rc | tee ${WORKDIR}/translate_crash.log


# Traige the crashes generated by Hopper with casr
echo "==================================="
echo "==  Crash triage"
echo "==================================="
./do_casr.sh $rc | tee ${WORKDIR}/casr.log


# Re-compile Hopper's harnesses and collect coverage 
echo "==================================="
echo "==  Starting coverage trials"
echo "==================================="
./do_coverage.sh $rc | tee ${WORKDIR}/coverage.log


